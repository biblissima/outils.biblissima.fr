						<section role="tabpanel" class="tab-pane fade" id="collaboration">
							<h1>Work in collaboration (PluCo plugin)</h1>
							<p>PluCo (PLUgin COllaboratif) is a module developed in Java allowing <strong>collaborative editing work on remote XML resources</strong>.</p>
							
							<p>PluCo enables:</p>
							<ul>
								<li>the creation and collaborative manipulation of XML resources ;</li>
								<li>collaborative indexing by connecting to authority lists (local, national or international).</li>
							</ul>

							<p>
								<a title="PluCo (PLUgin COllaboratif)" href="http://www.unicaen.fr/recherche/mrsh/sites/default/files/public/document_numerique/pluco.tgz" class="btn"><i class="fa fa-download" aria-hidden="true"></i> Download PluCo</a>
								<br /> <small>PluCo (PLUgin COllaboratif) - Pôle Document Numérique, MRSH de Caen
								<br />
								<a href="http://www.xmlmind.com/xmleditor/download.shtml" target="_blank" rel="noopener nofollow">Download XXE</a></small>
							</p>
							
							<div class="well">
								<h3>Technical details:</h3>
								<ul>
									<li>The plugin must be integrated into an XML editor, it cannot work autonomously.</li>
									<li>The resources must be stored on an XML server accessible in REST and compliant with the XQuery Update standard (all tests have been performed on BaseX 8.0+ servers).</li>
									<li>It is natively functional with <a href="http://www.xmlmind.com/xmleditor/" target="_blank" rel="noopener nofollow">XMLMind Editor</a> but can be easily extended to be compatible with other XML editors and servers.</li>
								</ul>
							</div>

							<p><a href="http://www.unicaen.fr/recherche/mrsh/document_numerique/outils/pluco">Presentation of the tool</a> (MRSH of Caen)</p>

							<p>PluCo is made available under the <a rel="license" href="http://www.cecill.info/licences/Licence_CeCILL-C_V1-fr.html" target="_blank" rel="noopener nofollow">CeCILL-C</a> open source licence, it is developed by the <a href="http://www.certic.unicaen.fr" target="_blank" rel="noopener">CERTIC</a> and the <a href="http://www.unicaen.fr/recherche/mrsh/document_numerique/" target="_blank" rel="noopener">Pôle Document Numérique</a>, with the support of Biblissima.</p>
