						<section role="tabpanel" class="tab-pane fade" id="publication">
							<h1>Publish, disseminate</h1>
							<p>MaX is an <strong>XML Display Engine</strong>. It relies on <a href="http://www.basex.org" target="_blank" rel="noopener nofollow">BaseX</a>, the XML database engine, and the <a href="http://docs.basex.org/wiki/RESTXQ" target="_blank" rel="noopener nofollow">RESTXQ</a> and <a href="https://www.w3.org/TR/xslt20/" target="_blank" rel="noopener nofollow">XSLT</a> technologies.</p>

							<p><a title="MaX (XML Display Engine)" href="http://www.unicaen.fr/recherche/mrsh/sites/default/files/public/document_numerique/MaX.tgz" class="btn"><i class="fa fa-download" aria-hidden="true"></i> Download MaX</a>
								<br /> <small>MaX (XML Display Engine) - Pôle Document Numérique, MRSH of Caen</small>
							</p>

							<p>MaX is a generic and extensible tool capable of producing reading interfaces for all XML standards. However, by default it offers a number of modules for the <a href="http://www.tei-c.org" target="_blank" rel="noopener nofollow">TEI</a>.</p>

							<p>MaX offers page making solutions that respect the principles of responsive design. It provides a search engine based on <a href="https://www.w3.org/TR/xquery-3/" target="_blank" rel="noopener nofollow">XQuery</a> configurable and adaptable to the XML sources that need to be manipulated, as well as a  pager to display large resources efficiently.

							<p>It includes the <a href="https://www.mathjax.org" target="_blank" rel="noopener nofollow">MathJax</a> modules for equations and <a href="https://openseadragon.github.io" target="_blank" rel="noopener nofollow">OpenSeadragon</a> for the images.</p>
							
							<p>MaX offers by default, in addition to the support of the basic TEI elements (generic divisions, tables, figures, paragraphs, notes, etc.), the following modules:</p>
							<ul>
								<li>critical apparatus management ;</li>
								<li>corrections management ;</li>
								<li>index generation ;</li>
								<li>alignment of texts versions (e.g. transcription/translation).</li>
							</ul>

							<p><a href="http://www.unicaen.fr/recherche/mrsh/document_numerique/outils/max">Presentation of the tool</a> (MRSH of Caen)</p>

							<p>MaX is made available under the <a rel="license" href="http://www.cecill.info/licences/Licence_CeCILL-B_V1-fr.html" target="_blank" rel="noopener nofollow">CeCILL-B</a> open source licence, it is developed by the <a href="http://www.certic.unicaen.fr" target="_blank" rel="noopener">CERTIC</a> and the <a href="http://www.unicaen.fr/recherche/mrsh/document_numerique/" target="_blank" rel="noopener">Pôle Document Numérique</a>, with the support of Biblissima.</p>
