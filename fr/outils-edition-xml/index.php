						<section role="tabpanel" class="tab-pane fade" id="collaboration">
							<h1>Travailler en collaboration (plugin PluCo)</h1>
							<p>PluCo (PLUgin COllaboratif) est un module développé en Java permettant le <strong>travail d’édition collaboratif sur des ressources XML distantes</strong>.</p>
							
							<p>PluCo permet :</p>
							<ul>
								<li>la constitution et manipulation collaborative de ressources XML ;</li>
								<li>l'indexation collaborative par la connexion à des référentiels (locaux, nationaux ou internationaux).</li>
							</ul>

							<p>
								<a title="PluCo (PLUgin COllaboratif)" href="http://www.unicaen.fr/recherche/mrsh/sites/default/files/public/document_numerique/pluco.tgz" class="btn"><i class="fa fa-download" aria-hidden="true"></i> Télécharger PluCo</a>
								<br /> <small>PluCo (PLUgin COllaboratif) - Pôle Document Numérique, MRSH de Caen
								<br />
								<a href="http://www.xmlmind.com/xmleditor/download.shtml" target="_blank" rel="noopener nofollow">Télécharger XXE</a></small>
							</p>
							
							<div class="well">
								<h3>Détails techniques :</h3>
								<ul>
									<li>Le plugin doit être intégré à un éditeur XML, il ne peut donc pas fonctionner de manière autonome.</li>
									<li>Les ressources doivent être stockées sur un serveur XML accessible en REST et respectant la norme XQuery Update (l’intégralité des tests ont été effectués sur des serveurs BaseX 8.0+).</li>
									<li>Il est nativement fonctionnel avec <a href="http://www.xmlmind.com/xmleditor/" target="_blank" rel="noopener nofollow">XMLMind Editor</a> mais peut cependant être étendu facilement en vue d’être compatible avec d’autres éditeurs et serveurs XML.</li>
								</ul>
							</div>

							<p><a href="http://www.unicaen.fr/recherche/mrsh/document_numerique/outils/pluco">Présentation de l'outil</a> (MRSH de Caen)</p>

							<p>PluCo est régi par la licence open source <a rel="license" href="http://www.cecill.info/licences/Licence_CeCILL-C_V1-fr.html" target="_blank" rel="noopener nofollow">CeCILL-C</a>, il est développé par le <a href="http://www.certic.unicaen.fr" target="_blank" rel="noopener">CERTIC</a> et le <a href="http://www.unicaen.fr/recherche/mrsh/document_numerique/" target="_blank" rel="noopener">Pôle Document Numérique</a>, avec le soutien de Biblissima.</p>
