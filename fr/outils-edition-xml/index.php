							<h1>Editer des inventaires anciens en XML-TEI</h1>
							<p>Dans le cadre de l’Equipex Biblissima, le Pôle Document numérique de la MRSH de Caen a participé à un travail de réflexion sur <strong>l’encodage en XML d’inventaires anciens</strong>. Il a ensuite réalisé un <strong>environnement d’édition électronique scientifique en XML-TEI de listes de livres de bibliothèques anciennes</strong>, compatible pour la publication de sources dans la collection en ligne <strong><a href="https://www.unicaen.fr/services/puc/sources/thecae/accueil">Thecae</a></strong> aux Presses Universitaires de Caen.</p>

