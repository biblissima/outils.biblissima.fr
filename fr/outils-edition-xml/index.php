							<p>Le Pôle Document numérique de la <abbr title="Maison de la Recherche en Sciences Humaines">MRSH</abbr> de l'Université de Caen-Normandie élabore plusieurs outils et environnements pour travailler en XML sur des documents anciens :</p>
