	    <h1>Boîte à outils Biblissima (Baobab) <small class="text-danger">Site en construction…</small></h1>
	    <p class="lead text-muted">
	        Ouverture prévue courant 2017.
	    </p>
        <p>La <strong>boîte à outils Biblissima (Baobab)</strong> est un ensemble cohérent et raisonné de ressources, outils, guides et tutoriels apportant une <strong>aide à la production et la collecte de données</strong> sur les thématiques couvertes par Biblissima : <strong>la circulation des textes, le devenir des bibliothèques et la transmission des savoirs en Europe du VIIIe au XVIIIe siècle</strong>.</p>
        <p>La boîte à outils est conçue pour un public large et diversifié : chercheurs, ingénieurs, personnel des bibliothèques et des archives (conservateurs, bibliothécaires), professeurs, étudiants, public cultivé. Toute personne qui travaille avec des documents et des manuscrits médiévaux ou des imprimés anciens, ou qui s’y intéresse, pourra s’y reporter. Cependant, la cible prioritaire est tous ceux qui sont amenés à enrichir les <a href="https://projet.biblissima.fr/fr/projet/volet-2-corpus-donnees">données de Biblissima</a>, que ce soit par <strong>la saisie de données, le catalogage de manuscrits ou d’imprimés, la transcription, l’édition ou la traduction des textes</strong>, entre autres.</p>
        <p>La réalisation de ce site s'inscrit dans le Volet 1 du projet Biblissima : <a href="https://projet.biblissima.fr/fr/projet/volet-1-observatoire-biblissima">l'Observatoire Biblissima</a>.</p>
